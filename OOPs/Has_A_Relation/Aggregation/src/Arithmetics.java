public class Arithmetics {

    double half(double num) {
        num/=2;
        return num;
    }

    double sqr(double num) {
        num*=num;
        return num;
    }

    double mul(double num1, double num2) {
        num1*=num2;
        return num1;
    }

    double div(double dividend, double divisor) {
        double quotient = dividend/divisor;
        return quotient;
    }

}
